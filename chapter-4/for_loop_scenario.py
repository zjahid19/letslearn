# List of all traffic breakers on the way
all_breaker_list = ['A', 'B', 'C', 'D']

# Start the journey
print('Starting the journey from home to office...')

# Loop through each breaker
for breaker in all_breaker_list:
    print(f'Reached speed breaker {breaker}.')
    print('Slow down your car and move forward carefully!')

# End of the journey
print('Reached office after crossing all speed breakers.')
