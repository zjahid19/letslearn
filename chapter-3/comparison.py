number_1 = 50  
number_2 = 100  

# Equal to operator ==  
equal_result = (number_1 == number_2)  
print('Equal to result is:', equal_result)  

# Not equal to operator !=  
not_equal_result = (number_1 != number_2)  
print('Not equal to result is:', not_equal_result)  

# Less than operator <  
less_than_result = (number_1 < number_2)  
print('Less than result is:', less_than_result)  

# Greater than operator >  
greater_than_result = (number_1 > number_2)  
print('Greater than result is:', greater_than_result)  

# Less than or equal to operator <=  
less_than_equal_result = (number_1 <= number_2)  
print('Less than or equal to result is:', less_than_equal_result)  

# Greater than or equal to operator >=  
greater_than_equal_result = (number_1 >= number_2)  
print('Greater than or equal to result is:', greater_than_equal_result)  