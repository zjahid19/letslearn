number_1 = 50
number_2 = 20 

# Addition of two numbers
addition_result = number_1 + number_2
print('The addition of two numbers {} and {} is: {}'.format(number_1,number_2,addition_result))

# Subtraction of two numbers
subtraction_result = number_1 - number_2
print('The subtraction of two numbers is:', subtraction_result)

# Multiplication of two numbers
multiplication_result = number_1 * number_2
print('The multiplication of two numbers is:', multiplication_result)

# Division of two numbers
division_result = number_1 / number_2
print('The division of two numbers is:', division_result)

# Floor Division of two numbers
floor_division_result = number_1 // number_2
print('The floor division of two numbers is:', floor_division_result)

# Modulus of two numbers
modulus_result = number_1 % number_2
print('The modulus of two numbers is:', modulus_result)

# Exponentiation of two numbers
exponentiation_result = number_1 ** number_2
print('The exponentiation of two numbers is:', exponentiation_result)